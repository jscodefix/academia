export function uniqChildren(regions) {
  // Flatten the regions array into a single array of offices.
  const offices = regions.flatMap(region => region.offices);

  // Create a map to store the unique office objects.
  const uniqueOfficeObjects = new Map();

  // Iterate over the offices array and add each office object to the map.
  offices.forEach(office => {
    uniqueOfficeObjects.set(office.code, office);
  });

  // Return an array of the unique office objects.
  return [...uniqueOfficeObjects.values()];
}
